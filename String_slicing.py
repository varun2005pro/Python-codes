A="Hello, World!"  #slicing
print(A[2:5])        #The first character has index 0.

B="Hello, World!"   #Slice From the Start
print(B[:6])          #By leaving out the start index, the range will start at the first character

C="Hello, World!" #Slice to the end
print(C[4:])      #By leaving out the end index, the range will go to the end:

D="Hello, World!"#Negative indexing 
print(D[-5:-2])     #Use negative indexes to start the slice from the end of the string

